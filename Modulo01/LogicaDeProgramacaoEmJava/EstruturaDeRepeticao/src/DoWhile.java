public class DoWhile {
    public static void main(String[] args) {
        int x = 1;

        do {
            System.out.println("Contador: " + x);
            x++;
        } while(x <= 5);

        int y = 55;
        do {
            System.out.println("Contador: " + y);
            y++;
        } while (y <= 5);
    }
}
