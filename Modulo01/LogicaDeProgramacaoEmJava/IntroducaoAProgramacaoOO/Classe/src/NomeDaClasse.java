public class NomeDaClasse {
    // Atributos
    private String atributo1;
    private int atributo2;

    // Construtor

    public NomeDaClasse(String atributo1, int atributo2) {
        this.atributo1 = atributo1;
        this.atributo2 = atributo2;
    }

    // Metodos
    public void metodo1() {
        System.out.println("Metodo1" + atributo1);
    }

    public void metodo2() {
        System.out.println("Metodo2" + atributo2);
    }
}
