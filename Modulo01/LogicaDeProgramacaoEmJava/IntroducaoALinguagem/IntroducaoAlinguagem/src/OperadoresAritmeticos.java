public class OperadoresAritmeticos {
    public static void main(String[] args) {
        double x = 10;
        double y = 4;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
    }
}
