package Operadores;

public class Aritmetico {
    public static void main(String[] args) {
        int soma = 47 + 15; // Operação de adição
        int subtracao = 47 - 15; // Operação de subtração
        int multiplicacao = 5 * 4; // Operação de multiplicação
        int divisaoInteira = 10/3; // Operação de divisão inteira
        double divisaoReal = 10.0/3.0; // Operação de divisão real
        int resto = 10 % 3; // Operação de resto

        System.out.println(soma);
        System.out.println(subtracao);
        System.out.println(multiplicacao);
        System.out.println(divisaoInteira);
        System.out.println(divisaoReal);
        System.out.println(resto);

        // Regas de conversão autmática
        // Um operando é double, o outro será convertido para double
        //

    }
}
