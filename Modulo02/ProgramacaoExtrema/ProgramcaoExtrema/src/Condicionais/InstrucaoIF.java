package Condicionais;

public class InstrucaoIF {
    public static void main(String[] args) {
        int x = 10;

        if(x == 10){
            System.out.println("O valor é 10");
        }


        if(x > 10){
            System.out.println("O valor é maior que 10");
        } else {
            System.out.println("O valor é menor ou igual a 10");
        }
    }
}
